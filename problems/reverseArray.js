/**
 * Escribe una función que reciba un array y devuelva un nuevo array con los elementos en orden inverso.
 * @param {any[]} arr - Array de elementos.
 * @returns {any[]} - Nuevo array con los elementos en orden inverso.
 */
export const reverseArray = (arr) => {
  // Aquí puedes añadir más lógica si lo deseas
  return arr.toReversed();
};
